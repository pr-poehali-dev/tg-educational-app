function buildAudiEcus(options?: {
  hasEpb?: boolean;
  hasAirSuspension?: boolean;
  hasQuattro?: boolean;
}): EcuDefinition[] {
  const ecus: EcuDefinition[] = [
    audiEngineEcu,
    audiTcmEcu,
    audiAbsEcu,
    audiSrsEcu,
    audiKombiEcu,
    audiEpsEcu,
    audiClimateEcu,
    audiGatewayEcu,
  ];
  if (options?.hasEpb) ecus.push(audiEpbEcu);
  if (options?.hasAirSuspension) ecus.push(airSuspensionEcu);
  if (options?.hasQuattro) ecus.push(quattroEcu);
  return ecus;
}

